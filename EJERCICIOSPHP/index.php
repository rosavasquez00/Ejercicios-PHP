        <h2 style="margin-left: 25px">¿Qué es PHP?</h2>
        <p style="margin-left: 12px">PHP es un lenguaje de programación de código abierto del lado del servidor que se utiliza principalmente para
        crear páginas web dinámicas. La abreviatura nació originariamente de “Personal Home Page Tools”, aunque
        hoy en día se ha convertido en el acrónimo recursivo para “PHP:Hypertext Preprocessor”.</p><br>
        
        <p style="margin-left: 12px">Mientras que los lenguajes del lado del cliente como HTML, CSS o JavaScript son interpretados primero por  el navegador web en el momento de abrir una página, el código PHP se ejecuta en el servidor web. Allí, los  scripts de PHP generan el código HTML que se envía después al navegador. Este no recibe el código real (el  script de PHP), sino el resultado de la ejecución del mismo.</p>

